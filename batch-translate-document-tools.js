const localesDir = path.join(__dirname, 'public', 'locales');

// Function to update translation files
async function updateTranslationFiles() {
  console.log('🚀 Starting document tools translation update...');
  
  for (const [language, translationData] of Object.entries(translations)) {
    const filePath = path.join(localesDir, `${language}.json`);
    
    try {
      // Read existing file
      let existingData = {};
      if (fs.existsSync(filePath)) {
        const fileContent = fs.readFileSync(filePath, 'utf8');
        existingData = JSON.parse(fileContent);
      }
      
      // Add new translations
      const updatedData = { ...existingData, ...translationData };
      
      // Write back to file
      fs.writeFileSync(filePath, JSON.stringify(updatedData, null, 2));
      
      const newKeysCount = Object.keys(translationData).length;
      console.log(`✅ ${language}.json: Added ${newKeysCount} document dynamic text keys`);
      
    } catch (error) {
      console.error(`❌ Error updating ${language}.json:`, error.message);
    }
  }
  
  console.log('📊 Document Dynamic Translation Summary:');
  console.log(`   Total languages: ${Object.keys(translations).length}`);
  console.log(`   Keys per language: ${Object.keys(translations.en).length}`);
  console.log(`   Total translations added: ${Object.keys(translations).length * Object.keys(translations.en).length}`);
  console.log('✨ All document dynamic text translations completed!');
}

// Run the update
updateTranslationFiles().catch(console.error);