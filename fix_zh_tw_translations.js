// 简繁转换函数
function convertToTraditional(text) {
    let result = text;
    for (let [simplified, traditional] of Object.entries(zhCNToZhTW)) {
        result = result.replace(new RegExp(simplified, 'g'), traditional);
    }
    return result;
}

